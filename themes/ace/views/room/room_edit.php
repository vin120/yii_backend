function checkRoom(){
    flag = 1;
    $service_name = $("input[name='name']").val();
    $this_id = $("input[id='room_id']").val();
	$url = "<?php echo Yii::app()->createUrl("Room/RoomGetAgain")?>";
	$.ajax({
		type:'post',
		data:'title='+$service_name+'&this_id='+$this_id,
		url:$url,
		async: false,
		success:function(data){
		    if(data > 0){
				flag =  0;
		    }else{
				flag =  1;
			}
		}
	});
	return flag;
